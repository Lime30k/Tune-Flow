public class Song {
    String name;
    String genre;
    double lenght;
    String artist;
    int likes;
    String filepath;

    public Song(){

    }
    public String getName(){
        return name;
    }
    public String getGenre(){
        return genre;
    }

    public String getArtist() {
        return artist;
    }

    public String getFilepath() {
        return filepath;
    }

    public int getLikes() {
        return likes;
    }

    public double getLenght() {
        return lenght;
    }
}
